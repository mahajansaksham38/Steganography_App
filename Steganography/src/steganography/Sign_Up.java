    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false)
        {
            JOptionPane.showMessageDialog(rootPane, "Please select a Gender", "Error", JOptionPane.ERROR_MESSAGE);
            
        }
        else
        {
            if(jTextField1.getText().trim().length()>0 || jTextField2.getText().trim().length()>0 || jTextField3.getText().trim().length()>0 || jTextField4.getText().trim().length()>0 || jPasswordField2.getText().trim().length()>0 ||jPasswordField1.getText().trim().length()>0 )
         
        
        try
        {
            
                PreparedStatement ps = Steganography.con.prepareStatement("Insert into Customer values(?,?,?,?,?,?)");
                String password=new String(jPasswordField1.getPassword());
                String confirm=new String(jPasswordField2.getPassword());
                ps.setString(1, jTextField1.getText() );
                ps.setString(2, jTextField2.getText() );
                ps.setString(3, jTextField3.getText() );
                 ps.setString(4, jTextField4.getText() );
                 ps.setString(5, password);
                 ps.setString(6, confirm);
                 
                 if(password.equals(confirm))
                 {
                ps.execute();
                JOptionPane.showMessageDialog(rootPane, "Sign Up sucessful..!");
                this.dispose();
                 }
                 else
                 {
                     JOptionPane.showMessageDialog(rootPane, "Password don't match.!", "Unsucessul", JOptionPane.ERROR_MESSAGE);
                 
                 }
        }//try
        catch(SQLException | HeadlessException ex)
        {
            
            JOptionPane.showMessageDialog(rootPane, ex);
            
        }//Catch
        else
            {
             JOptionPane.showMessageDialog(rootPane, "Enter the required credentials", "Unsucessful", JOptionPane.ERROR_MESSAGE);
            }
        }



    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         JFileChooser file=new JFileChooser();  
        file.setDialogTitle("choose image");

        FileNameExtensionFilter filter=new  FileNameExtensionFilter("PNG Images","PNG"); 
        file.setFileFilter(filter);  
                       
       
        int result=file.showOpenDialog(null); 
        if(result==JFileChooser.APPROVE_OPTION) 
        {
            File fc=file.getSelectedFile();
            try
            {
                img=ImageIO.read(fc);
                JLabel lbl=new JLabel(new ImageIcon(img)); 
                jScrollPane1.getViewport().add(lbl);
                
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

